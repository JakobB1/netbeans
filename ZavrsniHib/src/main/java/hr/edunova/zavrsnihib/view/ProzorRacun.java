    private void lstEntitetiValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstEntitetiValueChanged
        if(evt.getValueIsAdjusting() || lstEntiteti.getSelectedValue()== null){
            return;
        }
        
        obrada.setEntitet(lstEntiteti.getSelectedValue());
        var r = obrada.getEntitet();
        
        txtBrojRacuna.setText(r.getBrojRacuna());
        
        // postavljanje operatera u cmb
        cmbOperater.setSelectedItem(r.getOperater());
        
        DefaultListModel<Proizvod> mp = new DefaultListModel<>();
        r.getProizvodi().forEach(p->{mp.addElement(p);});
        lstProizvodiNaRacunu.setModel(mp);
        
    }//GEN-LAST:event_lstEntitetiValueChanged

    private void btnPromjeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPromjeniActionPerformed
        postaviVrijednostiUEntitet();
        try {
            obrada.update();
            ucitaj();
        } catch (EdunovaException ex) {
            JOptionPane.showMessageDialog(getParent(), ex.getPoruka());
        }
    }//GEN-LAST:event_btnPromjeniActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
        try {
            obrada.delete();
            ucitaj();
        } catch (EdunovaException ex) {
            JOptionPane.showMessageDialog(getParent(), ex.getPoruka());
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        obrada.setEntitet(new Racun());
        postaviVrijednostiUEntitet();

        try {
            obrada.create();
            ucitaj();
        } catch (EdunovaException ex) {
            JOptionPane.showMessageDialog(getParent(), ex.getPoruka());
        }
    }//GEN-LAST:event_btnDodajActionPerformed

    private void btnTraziActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTraziActionPerformed
        DefaultListModel<Proizvod> mp = new DefaultListModel<>();
        obradaProizvod.read(txtUvjet.getText()).forEach(p -> {
            mp.addElement(p);
        });
        lstProizvodiUBazi.setModel(mp);
    }//GEN-LAST:event_btnTraziActionPerformed

    private void btnObrisiIzRacunaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiIzRacunaActionPerformed
        List<Polaznik> lista = lstPolazniciNaGrupi.getSelectedValuesList();

        for (Polaznik p : lista) {
            obrisiPolaznikaIzGrupe(p);
        }
        lstPolazniciNaGrupi.repaint();
    }//GEN-LAST:event_btnObrisiIzRacunaActionPerformed

    private void btnDodajURacunActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajURacunActionPerformed
        DefaultListModel<Polaznik> m = (DefaultListModel<Polaznik>) lstPolazniciNaGrupi.getModel();
        for (Polaznik p : lstPolazniciUBazi.getSelectedValuesList()) {
            if (!postojiPolaznikUModelu(m, p)) {
                m.addElement(p);
            }
        }
        lstPolazniciNaGrupi.repaint();
    }//GEN-LAST:event_btnDodajURacunActionPerformed

    @Override
    public void postaviVrijednostiUEntitet() {
        var r = obrada.getEntitet();
        r.setBrojRacuna(txtBrojRacuna.getText());
    }

    @Override
    public void postavke() {
        setTitle(Aplikacija.getNaslov("Racuni"));
     
        
        DefaultComboBoxModel<Operater> mo = new DefaultComboBoxModel<>();
        Operater op = new Operater();
        op.setId(Long.valueOf(0));
        op.setIme("Nije");
        op.setPrezime("Odabrano");
        mo.addElement(op);
        new ObradaOperater().read().forEach(o->{mo.addElement(o);});
        cmbOperater.setModel(mo);
        
        DefaultComboBoxModel<Korisnik> mk = new DefaultComboBoxModel<>();
        new ObradaKorisnik().read().forEach(k->{mk.addElement(k);});
        Korisnik k = new Korisnik();
        k.setId(Long.valueOf(0));
        k.setIme("Nije");
        k.setPrezime("odabran" );
        mk.addElement(k);
        cmbKorisnik.setModel(mk);
    }

    @Override
    public void ucitaj() {
        DefaultListModel<Racun> mr = new DefaultListModel<>();
        obrada.read().forEach(r->{mr.addElement(r);});
        lstEntiteti.setModel(mr);
    }

    

