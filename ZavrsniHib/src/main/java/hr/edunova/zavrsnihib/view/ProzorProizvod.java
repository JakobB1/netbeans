    @Override
    public void postaviVrijednostiUEntitet() {
        var p = obrada.getEntitet();
        p.setNaziv(txtNaziv.getText());
        p.setCijena(BigDecimal.TEN);
        p.setKodArtikla(txtKodArtikla.getText());
        p.setOpisProizvoda(txtOpisProizvoda.getText());
        p.setDostupnost(chbDostupnost.isSelected());
        
    }

    @Override
    public void postavke() {
        setTitle(Aplikacija.getNaslov("Proizvodi"));
        
        
    }

    @Override
    public void ucitaj() {
        DefaultListModel<Proizvod> m = new DefaultListModel<>();
        
        obrada.read().forEach(p->{m.addElement(p);});
        
        lstEntiteti.setModel(m);
    }
